# peça ao usuário o tamanho dos lados de um retângulo e exiba a área do retângulo
largura = float(input("📏 Ô sô, quar é a largura desse retângu aí?: "))
altura = float(input("📐 E a artura, fi? Me fala aí: "))
area = largura * altura
print(f"📊 Uai, a área desse trem deu: {area} m² \n😎 Tá certin, viu?!")
